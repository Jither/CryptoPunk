      subroutine generate_testvector(kstr,mstr)
        character*32 mstr
        character*32 kstr
        character*32 cstr
        dimension k(0:7,0:15),m(0:7,0:7,0:1)
        dimension kb(0:31),mb(0:31)
        dimension key(0:127),message(0:127)
        equivalence (k(0,0),key(0)),(m(0,0,0),message(0))

        read(kstr,2001) (kb(i),i=0,31)
        read(mstr,2001) (mb(i),i=0,31)

        call expand(message,mb,32)
        call expand(key,kb,32)

c       encipher
        d=0
        call lucifer(d,k,m)

        call compress(message,mb,32)
        call compress(key,kb,32)

        write(cstr,2001) (mb(i),i=0,31)

        write(6,2000) mstr,kstr,cstr

2000    format(a32,' ',a32,' ',a32)
2001    format(32z1.1)
      end

      subroutine generate_exercise()
        implicit integer (a-z)
        character*32 mstr
        character*32 kstr
        dimension k(0:7,0:15),m(0:7,0:7,0:1)
        dimension kb(0:31),mb(0:31)
        dimension res(0:31)
        dimension key(0:127),message(0:127)
        equivalence (k(0,0),key(0)),(m(0,0,0),message(0))

        kstr = '0123456789abcdeffedcba9876543210'
        mstr = '00000000000000000000000000000000'

        read(kstr,3001) (kb(i),i=0,31)
        read(mstr,3001) (mb(i),i=0,31)

        write(6, 3002) kstr, mstr

c       encipher
        d=0

        do 100 i=0,999,1
          call expand(message,mb,32)
          call expand(key,kb,32)

          call lucifer(d,k,m)

          call compress(message,mb,32)
          call compress(key,kb,32)

          res = mb
          mb = kb
          kb = res
100     continue

        write(mstr,3001) (res(i),i=0,31)

        write(6,3003) mstr

3001    format(32z1.1)
3002    format('key   : ',a32,' plain: ',a32)
3003    format('cipher: ',a32)
      end
